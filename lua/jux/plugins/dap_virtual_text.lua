-- ----------------------------------------------------------------------------
-- nvim-dap-virtual-text configuration for Neovim
-- ----------------------------------------------------------------------------
local M = {}

-- ----------------------------------------------------------------------------
-- Key mappings
-- ----------------------------------------------------------------------------
M.keys = {
    {
        "<leader>dtv",
        function()
            require("nvim-dap-virtual-text").toggle()
        end,
        desc = "toggle dap virtual text",
    },
}

-- ----------------------------------------------------------------------------
-- Setup function
-- ----------------------------------------------------------------------------
function M.setup()
    require("nvim-dap-virtual-text").setup({
        enabled = false,
    })
end

return M
