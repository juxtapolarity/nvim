-- ----------------------------------------------------------------------------
-- telescope-undo configuration for Neovim
-- ----------------------------------------------------------------------------
local M = {}

-- ----------------------------------------------------------------------------
-- Key mappings
-- ----------------------------------------------------------------------------
M.keys = {
    {
        "<leader>u",
        function()
            require("telescope").extensions.undo.undo()
        end,
        desc = "telescope undo",
    },
}

-- ----------------------------------------------------------------------------
-- Setup function
-- ----------------------------------------------------------------------------
function M.setup()
    require("telescope").load_extension("undo")
end

return M
